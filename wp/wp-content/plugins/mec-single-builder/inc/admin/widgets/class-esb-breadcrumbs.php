			'hr2',
			[
				'type' => \Elementor\Controls_Manager::DIVIDER,
			]
		);

		$this->add_responsive_control(
			'mec_breadcrumbs_arrow',
			[
				'label' 		=> __( 'Arrow Size', 'mec-single-builder' ),
