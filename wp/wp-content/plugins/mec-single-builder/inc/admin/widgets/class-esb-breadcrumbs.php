	/**
	 * Render Alert widget output on the frontend.
	 *
	 *
	 * @since 1.0.0
	 * @access protected
	 */
	protected function render() {


		echo \MEC\SingleBuilder\SingleBuilder::getInstance()->output( 'event-breadcrumbs' );
	}

}