        <div class="section-content">

            <p>This form is intended as an example of a repository that holds the information required for a medical
                appraisal. It has been designed with the appraisal meeting in mind, in a logical manner that mirrors how
                the appraisal conversation may flow. It is also intended as a practical demonstration of the information
                in the <span class="text-blue">Medical Appraisal Guide</span> and the <span class="text-blue">medical
                    appraisal for registration in a company, version 4 (NHS Revalidation Support Team, 2015)</span>.</p>

            <p>This version of the form was last updated on <strong>September 2018</strong>.</p>

            <p>The form was updated in 2016 with an aim to improve its usability and enhance the appraisal outputs. It
                is now an annual form only.</p>

            <p>All aspects of a medical practitioner's role can be, and should be, detailed within this form, including
                clinical, managerial and academic work, research, private practice, locum work and voluntary roles. Your
                main focus throughout the appraisal process should be your reflection on both your activity and the
                outcomes as a result. For any supporting information you include, you are encouraged to consider this
                from a quality versus quantity perspective.</p>

            <p>Doctors should complete up to and including section 17 and submit the package of information to the
                appraiser by a mutually agreed date (which is usually at least two weeks before the appraisal). Sections
                18, 19 and 20 will be completed during and immediately after the appraisal meeting by both the doctor
                and the appraiser.</p>

            <p><strong>How does the form work?</strong></p>

            <ul>
                <li>It is an interactive pdf which allows you to type information into boxes and upload/attach documents
                    to the form.</li>
                <li>To navigate through the various sections you must either use the navigation bar down the left hand
                    side or use the <button class="button-small">Previous section</button> and <button
                        class="button-small">Next section</button> buttons at the end of each section.</li>
                <li>Once boxes are each of the tables allow you to attach documents or log that you intend to provide a
                    document separately. Documents are attached in the same way as you would attach a file to an email.
                </li>
                <li>The maximum size of the form cannot exceed 10MB due to restrictions with many email servers. Section
                    15 lists all of the supporting information that you have said you will provide. In the case of
                    attachments, it lists the size of each one and a running total of the space used for your
                    information.</li>
                <li>To be able to use the form, you will require at least version XI (eleven) of Adobe Reader installed
                    on your computer/device. Attempting to not be compatible and you may experience issues if you are
                    not using the correct software. NB: Internet browser pdf readers: If you are downloading this form
                    via the internet, your computer may attempt to open using your web browser, which will not provide
                    full functionality. If you have the correct version of Adobe Reader installed on your computer, and
                    depending on your computer's settings, To overcome this, instead of clicking on the download link,
                    right click on the link and choose 'save as' or 'save target as' and save the form to your hard
                    drive. You should then be able to open the form directly from your hard drive.</li>
            </ul>

            <p><strong>Saving the form</strong></p>
            <ul>
                <li>The first time you use this form, you may wish to complete sections 3 and 4 and save this as your
                    appraisal template to use each year.</li>
                <li>Remember to use the <button class="button-green">Save as</button> button relevant information to
                    them where required.</li>
                <li>For each year's appraisal, save your template with a different filename and then use the <button
                        class="button-green">Save form</button> buttons throughout the document to save your work as you
                    go.</li>
                <li>You can use the standard Windows Save As function which will ask you if you want to overwrite the
                    current file. It is OK to do this - you do not need to save a new version every time.</li>
                <li>If you do not wish to save over your template, save the form with a different filename.</li>
                <li>Save a copy at regular intervals.</li>
                <li>Section 21 allows you to save, lockdown and print out the form. You can also print out a number of
                    different section options, including an Appraisal Outputs Report (sections 3, 4, 18, 19 and 20),
                    which can also be saved separately if you have a basic pdf converter/writing software
                    available/loaded onto your computer, or you can use <span class="text-blue">CutePDF</span> (free)
                    via google.</li>
            </ul>

            <p><strong>Submitting supporting information</strong></p>
            <ul>
                <li>Many doctors will wish to submit supporting information including Word and Excel documents,
                    PowerPoint slides, pdfs and image files. Some file types may not be compatible with the form and if
                    so try to upload one at a time, a warning box will tell you that it is not possible.</li>
                <li>Zip files and webpages cannot be uploaded.</li>
                <li>If you wish to provide any audit reports or other supporting information to your appraiser,
                    presentations that include a lot of graphics or some types of scanned documents. These can either be
                    emailed to the appraiser separately, or in some cases, means of self-directed learning.</li>
                <li>If you intend to provide any files separately you should still list them in the appropriate section,
                    and comment on them. Make sure you remember to ask for confirmation that the appln has been received
                    and whether they found it useful, information to put in the Section 15 cumulative table.</li>
                <li>If you change your mind regarding an attachment, you will need to delete the row in the table and
                    add a new one. You can copy and paste any text that you have written first, and then attach a new
                    document.</li>
                <li>If you wish to <span class="text-blue">attach</span> a document that you have already attached, do
                    not click on <button class="button-small">Browse</button> and open the attachment from within the
                    form. You must first <button class="button-small">Delete</button> attached file at the bottom of the
                    document where it is originally saved.</li>
            </ul>

            <p><strong>Helpful hints</strong></p>
            <ul>
                <li>An asterisk (*) next to a question denotes that this field is compulsory and you will not be able to
                    submit the form at the end unless it is completed.</li>
                <li>The <span class="text-blue">?</span> buttons provide more information about what should be included
                    in each section or field. Many contain hyperlinks to further sources of information.</li>
                <li>The <span class="text-blue">+</span> button allows you to add additional rows to your tables.</li>
                <li>Hovering the mouse over the buttons in the navigation bar at the side of each section will show you
                    the name of each section, as listed in the contents page.</li>
            </ul>

            <ul>
                <li>All text boxes expand so you can write as much as you like although some have restrictions on the
                    amount of information you can add. You can also copy and paste information into them.</li>
                <li>When adding rows to tables using the <button class="button-green">+</button> button, be sure to
                    complete your writing before adding the attachment or logging the supporting information.</li>
                <li>Be careful when removing rows using the <button class="button-red">-</button> button, you do not get
                    a reminder, it will delete immediately!</li>
                <li><button class="button-small">Expand All</button> sections in the yellow and blue boxes will expand
                    yellow and/or blue boxes.</li>
                <li>Do not email the form onwards using the Adobe Reader menu bar "File", "Send File...", nor the "Send
                    file as email attachment" icon. This will result in a warning message and the form will not transfer
                    as an attached file when you attempt to email it. You should email the form from your email
                    application and attach the form directly from where it is filed.</li>
            </ul>

            <p>Once you have completed all the sections, the form can be transferred to your appraiser via email or for
                the process agreed within your designated body.</p>

            <p>If you have any issues with the form, or queries about how to use it, you should contact the appropriate
                revalidation/appraisal lead in your designated body or your appraiser.</p>

