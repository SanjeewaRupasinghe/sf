                <p><strong>Confidentiality and information governance</strong></p>
                <p>You must take care to abide by local confidentiality, data security and information governance
                    protocols when adding details into this form. In particular, you must remove all personally and
                    patient identifiable data. If you are unable to do this for specific items of information, you
                    should log the existence of the information in this form but provide the information itself
                    separately to your appraiser.</p>

                <p>Remember, you are providing this information primarily for your appraiser. After your appraisal is
                    signed off, your responsible officer or other person with appropriately delegated authority will
                    receive a copy of the form, specifically to read the appraisal outputs (the PDP and summary). Your
                    responsible officer should also keep a copy of the form and all of your supporting information on
                    file. Your responsible officer can provide you with more information about the circumstances under
                    which they may view this form.</p>

